
public class Contact {

	private String name;
	private int age;
	private String tel;
	
	// 持失切, getter五社球
	public Contact(String name, int age, String tel) {
		this.name = name;
		this.age = age;
		this.tel = tel;
	}

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public String getTel() {
		return tel;
	}
	
	
	
	
}
